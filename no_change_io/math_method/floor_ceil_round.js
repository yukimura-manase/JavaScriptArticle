// JavaScriptのMathオブジェクトには、数値を整数に丸めるためのメソッドが用意されています。

// Math.floor: 小数点以下を切り捨てる
// Math.ceil: 小数点以下を切り上げる
// Math.round: 小数点以下を四捨五入する

console.log(Math.floor(1.95));
// Expected output: 1

console.log(Math.floor(1.05));
// Expected output: 1

console.log(Math.ceil(1.95));
// Expected output: 2

console.log(Math.ceil(1.05));
// Expected output: 2

console.log(Math.round(3.141592)); // 3
